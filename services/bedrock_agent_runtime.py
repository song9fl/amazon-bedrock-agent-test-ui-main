import boto3
from botocore.exceptions import ClientError
import re

def invoke_agent(agent_id, agent_alias_id, session_id, prompt):
    try:
        # Initialize the client for the Bedrock agent runtime
        client = boto3.session.Session().client(service_name="bedrock-agent-runtime")
        
        # Invoke the agent with provided parameters
        response = client.invoke_agent(
            agentId=agent_id,
            agentAliasId=agent_alias_id,
            enableTrace=True,
            sessionId=session_id,
            inputText=prompt,
        )

        # Log the response to understand its structure
        print("Agent response:", response)  # Debugging line to inspect response structure

        # Extract the main response text from 'result'
        output_text = response.get("result", "").strip()
        citations = []
        trace = response.get("trace", {})  # Ensure trace is always defined

        # If there is a response in 'result', clean it up by removing inline placeholders
        if output_text:
            output_text = re.sub(r'%?\[\d+\]%?', '', output_text).strip()
        else:
            output_text = "No meaningful response was generated by the chatbot."

        # Process completion events to gather citations if they exist
        for event in response.get("completion", []):
            chunk = event.get("chunk")
            if chunk and "attribution" in chunk:
                for citation in chunk["attribution"]["citations"]:
                    # Verify citation structure before accessing it
                    if (
                        isinstance(citation, dict) and
                        "location" in citation and 
                        "s3Location" in citation["location"] and 
                        "uri" in citation["location"]["s3Location"]
                    ):
                        uri = citation["location"]["s3Location"]["uri"]
                        if uri not in citations:
                            citations.append(uri)
                    else:
                        print("Unexpected citation format:", citation)  # Debugging line

        # Append citations at the end of output_text if they exist
        if citations:
            citation_texts = "\n\nCitations:\n" + "\n".join([f"[{i+1}] {uri}" for i, uri in enumerate(citations)])
            output_text += citation_texts

    except ClientError as e:
        # Provide a user-friendly message in case of an error
        output_text = "An error occurred while trying to invoke the agent."
        print(f"ClientError: {e}")

    # Return the structured response with an ensured 'trace' key
    return {
        "output_text": output_text,
        "citations": citations,
        "trace": trace  # This will always be included, even if empty
    }
